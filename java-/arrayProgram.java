public class arrayProgram {
    public static void main(String[] args) {
        int[] arr = {10, 20, 30, 40, 50};

        // Access element
        System.out.println("Element at index 2: " + arr[2]);

        // Traverse array
        for (int i = 0; i < arr.length; i++) {
            System.out.print(arr[i] + " ");
        }
    }
}
